export type HttpStatusName = keyof typeof httpStatusCodes;
export type HttpStatusCode = (typeof httpStatusCodes)[HttpStatusName];

const httpStatusCodeSet = new Set<number>(Object.values(httpStatusCodes));

export const isHttpStatusCode = (value: number): value is HttpStatusCode =>
	httpStatusCodeSet.has(value);

export const getHttpStatusCode = (name: HttpStatusName): HttpStatusCode =>
	httpStatusCodes[name];

